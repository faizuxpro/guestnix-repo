// Pre-fetches default icon SVGs from Iconify API and writes them to defaults.ts
// Run with: node scripts/build-icon-defaults.mjs

import { writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

const DEFAULTS = {
  // Sections
  SECTION_DEFAULT: "ph:info-fill",

  // Bottom nav
  NAV_HOME: "ph:house-fill",
  NAV_GUIDE: "ph:book-open-fill",
  NAV_NEARBY: "ph:map-pin-fill",
  NAV_HOST: "ph:user-fill",
  NAV_FALLBACK: "ph:circle-fill",

  // Place categories (match src/lib/nearby-categories.ts keys)
  PLACE_RESTAURANT: "ph:fork-knife-fill",
  PLACE_CAFE: "ph:coffee-fill",
  PLACE_BAR: "ph:martini-fill",
  PLACE_GROCERY: "ph:shopping-cart-fill",
  PLACE_PARK: "ph:tree-fill",
  PLACE_BEACH: "ph:umbrella-simple-fill",
  PLACE_ATTRACTION: "ph:medal-fill",
  PLACE_MUSEUM: "ph:building-office-fill",
  PLACE_SHOPPING: "ph:shopping-bag-fill",
  PLACE_PHARMACY: "ph:pill-fill",
  PLACE_HOSPITAL: "ph:hospital-fill",
  PLACE_TRANSPORT: "ph:bus-fill",
  PLACE_GAS_STATION: "ph:gas-pump-fill",
  PLACE_GYM: "ph:barbell-fill",
  PLACE_OTHER: "ph:map-pin-fill",

  // Block fallbacks (used when host hasn't picked an icon for a sub-item)
  BLOCK_RULE: "ph:list-checks-fill",
  BLOCK_AMENITY: "ph:star-fill",
  BLOCK_EMERGENCY: "ph:warning-circle-fill",
  BLOCK_CONTACT: "ph:phone-fill",
  BLOCK_CALLOUT: "ph:info-fill",
  BLOCK_ALERT: "ph:warning-circle-fill",
  BLOCK_STACK_ITEM: "ph:info-fill",
};

async function fetchSvg(id) {
  const [prefix, name] = id.split(":");
  const url = `https://api.iconify.design/${prefix}/${name}.svg?color=currentColor`;
  const res = await fetch(url);
  if (!res.ok) throw new Error(`Failed ${id}: ${res.status}`);
  const text = await res.text();
  if (!text.startsWith("<svg")) throw new Error(`Bad svg ${id}: ${text.slice(0, 60)}`);
  const end = text.indexOf(">");
  return `${text.slice(0, end)} data-iconify="${id}" data-source="iconify"${text.slice(end)}`;
}

const out = {};
const errors = [];
for (const [key, id] of Object.entries(DEFAULTS)) {
  try {
    out[key] = await fetchSvg(id);
    console.log(`OK  ${key} <- ${id}`);
  } catch (err) {
    errors.push({ key, id, err: String(err) });
    console.error(`ERR ${key} <- ${id}: ${err}`);
  }
}

if (errors.length > 0) {
  console.error(`\n${errors.length} icons failed. Aborting.`);
  process.exit(1);
}

const lines = [
  "// Generated by scripts/build-icon-defaults.mjs",
  "// Do not edit manually. Run the script to regenerate.",
  "",
  "export const DEFAULT_ICONS = {",
];
for (const [key, svg] of Object.entries(out)) {
  const escaped = svg.replace(/`/g, "\\`").replace(/\${/g, "\\${");
  lines.push(`  ${key}: \`${escaped}\`,`);
}
lines.push("} as const;");
lines.push("");
lines.push("export type DefaultIconKey = keyof typeof DEFAULT_ICONS;");
lines.push("");

const outputPath = resolve(__dirname, "../src/lib/icons/defaults.ts");
writeFileSync(outputPath, lines.join("\n"), "utf-8");
console.log(`\nWrote ${Object.keys(out).length} icons to ${outputPath}`);
